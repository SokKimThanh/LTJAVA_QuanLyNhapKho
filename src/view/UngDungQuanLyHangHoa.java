    private void tblDSHangHoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDSHangHoaMouseClicked
        int colMaSP = 0;// column mã sản phẩm
        int colTenSP = 1;// column tên sản phẩm        
        int colGiaSP = 2;// column loai sản phẩm
        int colSoLuong = 3;// column giá sản phẩm
        int colNgaySX = 4;
        int colNoiSX = 5;
        DefaultTableModel tableModel = (DefaultTableModel) tblDSHangHoa.getModel();
        // chọn row trên table
        int row = tblDSHangHoa.getSelectedRow();

        // lấy giá trị từng cột
        String maSP = tableModel.getValueAt(row, colMaSP).toString();
        String tenSP = tableModel.getValueAt(row, colTenSP).toString();
        int giaSP = Integer.parseInt(tableModel.getValueAt(row, colGiaSP).toString());
        int soLuong = Integer.parseInt(tableModel.getValueAt(row, colSoLuong).toString());
        String ngaySX = tableModel.getValueAt(row, colNgaySX).toString();
        String noiSX = tableModel.getValueAt(row, colNoiSX).toString();
        // chọn xong nhập thông tin trên form
        txtMaHH.setText(maSP);
        txtTenHH.setText(tenSP);
        txtGiaBan.setText(giaSP + "");
        txtSoLuong.setText(soLuong + "");
        txtNgaySanXuat.setText(ngaySX);
        txtNoiSX.setText(noiSX);
    }//GEN-LAST:event_tblDSHangHoaMouseClicked

