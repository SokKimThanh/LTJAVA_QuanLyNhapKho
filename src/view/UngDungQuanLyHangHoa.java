    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if ("".equals(txtMaHH.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Chua chon ma hang hoa");
        }
        DefaultTableModel detbl = (DefaultTableModel) tblDSHangHoa.getModel();

        HangHoa hh = new HangHoa(txtMaHH.getText());
        boolean check = list.Xoa(hh);
        if (check) {
            JOptionPane.showMessageDialog(rootPane, "Xoa Thanh cong");
            detbl.removeRow(tblDSHangHoa.getSelectedRow());
            hienthi();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Xoa Khong Thanh cong");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

