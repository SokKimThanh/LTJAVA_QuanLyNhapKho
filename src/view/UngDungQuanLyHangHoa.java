    private void hienthi() {
        if (list.getData().isEmpty()) {
            return;
        }
        DefaultTableModel tableModel = (DefaultTableModel) tblDSHangHoa.getModel();
        tableModel.setRowCount(0);
        // TableDanhSachHangHoa = new JTable(data, columns);
        for (HangHoa value : list.getData()) {
            Object[] data;
            data = new Object[]{value.getMa(), value.getTen(), value.getDonGia(), value.getSoluong(), value.getNgaySX(), value.getNoisx()};
            tableModel.addRow(data);
        }

        tableModel.fireTableDataChanged();
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        if ("".equals(txtMaHH.getText())
                || "".equals(txtTenHH.getText())
                || "".equals(txtNgaySanXuat.getText())
                || "".equals(txtNoiSX.getText())
                || "".equals(txtGiaBan.getText())
                || "".equals(txtSoLuong.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Chua nhap day du du lieu truoc khi them moi");
        }
        try {
            HangHoa hh = new HangHoa();
            hh.setMa(txtMaHH.getText());
            hh.setTen(txtTenHH.getText());
            hh.setDonGia(Integer.parseInt(txtGiaBan.getText()));
            hh.setSoluong(Integer.parseInt(txtSoLuong.getText()));
            hh.setNgaySX(txtNgaySanXuat.getText());
            hh.setNoisx(txtNoiSX.getText());
            boolean check = list.Them(hh);
            if (check) {
                System.out.println("Them thanh cong");
                hienthi();
            } else {
                System.out.println("Them khong thanh cong");
                JOptionPane.showMessageDialog(rootPane, "Hang hoa da co trong danh sach");
            }
        } catch (NumberFormatException e) {
            String s = e.getMessage();
            System.out.println(e.getMessage());

        }
    }//GEN-LAST:event_btnThemActionPerformed

